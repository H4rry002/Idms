    private void signUPbuttonMouseClicked(java.awt.event.MouseEvent evt)  {//GEN-FIRST:event_signUPbuttonMouseClicked
        String password = new String(signUpPassword.getPassword());
        String cpassword = new String(signUpCPassword.getPassword());
        String email = signUpEmail.getText();
        String regisNo = signUpregisNo.getText();
        String name = signUpName.getText();
        Pattern pattern = Pattern.compile("^(?=.*[a-z])(?=.*[A-Z])(?=.*[0-9])(?=.*[@#$%^&*!=])(?=\\S+$).{8,}$");
        Matcher match = pattern.matcher(password);
        if(regisNo.length()<6){
            signUperror.setText("*Invalid Registration No or Gst No");
            return;
        }
        if(!match.matches()){
            signUperror.setText("Password should contain one Uppercase,lowercase,digit and a symbol");
            return;
        }
        if(!password.equals(cpassword)){
            signUperror.setText("*Password did not match");
            return;
        }
        if(person==null){
            signUperror.setText("*Please select your occupation");
            return;
        }
        try{
            String a = main.createUser(person, name, email, password, regisNo);

            if (a.substring(a.length() - 6, a.length() - 1).equals("email")) {
                signUperror.setText("*Email already registered");
            }
            if (a.substring(a.length() - 8, a.length() - 1).equals("PRIMARY")) {
                signUperror.setText("*Registration No or Gst No already registered");
            }
            if(a.startsWith("Inserted")){
                verifyErrorLabel.setForeground(new Color(49, 191, 49));
                verifyErrorLabel.setText("*Successful signed up");
                verifyEmailinput.setText(email);
//                verifyEmailinput.setEnabled(false);
                cardLayout.show(loginSignPanel,"card4");
                verified = false;
            }
        }catch (Exception e){
            e.printStackTrace();
        }
        if(signUperror.getText().length()>0) {
            new Thread(() -> {
                try {
                    Thread.sleep(4000);
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
                signUperror.setText("");
            }).start();
        }
    }//GEN-LAST:event_signUPbuttonMouseClicked

