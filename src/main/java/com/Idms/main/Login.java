    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed
        service.shutdown();
        String pass = new String(resetPassIn.getPassword());
        String cpass = new String(cResetPassIn.getPassword());
        Pattern pattern = Pattern.compile("^(?=.*[a-z])(?=.*[A-Z])(?=.*[0-9])(?=.*[@#$%^&*!=])(?=\\S+$).{8,}$");
        Matcher match = pattern.matcher(pass);
        
        if(!match.matches()) {
            resetPassworderror.setText("*Password should contain 1 Uppercase,1 Lowercase,1 Special char,1 Digit");
            return;
        }
        if(!pass.equals(cpass)){
            resetPassworderror.setText("*Your Password don't match");
            return;
        }
        isResetPassword = false;
        String a =  main.resetPassword(pass,username,person);
        if(a.equals("Password has been changed")){
            loginPassword.setText("");
            loginUsername.setText("");
            cardLayout.show(loginSignPanel,"card2");
            loginerror.setText(a);
            loginerror.setForeground(new Color(0, 245, 51));
            new Thread(()->{
                try {
                    Thread.sleep(4000);
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
                loginerror.setText("");
            }).start();
        }else{
            resetPassworderror.setText(a);
        }
        if(resetPassworderror.getText().length()>0){
            new Thread(() -> {
                try {
                    Thread.sleep(4000);
                } catch (InterruptedException e) {
                    System.out.println(e.toString());
                }
                resetPassworderror.setText("");
            }).start();
        }
    }//GEN-LAST:event_changePasswordButtonActionPerformed

