    String[][] todisplayTable(){
        appointmentNumValue.setText(String.valueOf(docPatientData.size()));
        if(docPatientData.size()==0){
            return new String[1][1];
        }
        String[][] patientTableData = new String[docPatientData.size()][4];
        for(int i=0;i<docPatientData.size();i++){
            Receipt r = docPatientData.get(i);
            patientTableData[i][0] = r.getPatientName();
            patientTableData[i][1] = String.valueOf(r.getPatientPhNo());
            patientTableData[i][2] = r.getMedicine();
            patientTableData[i][3] = String.valueOf(r.getGenerateTime());
        }
        return patientTableData;
    }
    ArrayList<Receipt> sortedList(String interval){
        ArrayList<Receipt> a = new ArrayList<>();
        Date date;
        if(allDocPatientData == null){
            return a;
        }
        if(allDocPatientData.size()==0){
            return a;
        }
        switch (interval) {
            case "OneYear" -> date = new Date(System.currentTimeMillis() - 365L * 24 * 3600 * 1000);
            case "OneMonth" -> date = new Date(System.currentTimeMillis() - 30L * 24 * 3600 * 1000);
            case "SixMonth" -> date = new Date(System.currentTimeMillis() - 182L * 24 * 3600 * 1000);
            case "FiveDays" -> date = new Date(System.currentTimeMillis() - 5L * 24 * 3600 * 1000);
            case "OneDay" -> date = new Date(System.currentTimeMillis() - 24 * 3600 * 1000);
            default -> throw new IllegalStateException("Unexpected value: " + interval);
        }
        for(Receipt r: allDocPatientData){
            if(r.getGenerateTime().after(date)){
                a.add(r);
            }
        }
        return a;
    }


