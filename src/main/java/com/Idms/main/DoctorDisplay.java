    void moveTimeIndicator(int x){
        Rectangle r = indicatorDate.getBounds();
        new Thread(() -> {
            int i=r.x;
                double velocity = .005;
            boolean direction = false;           // MOVE IN LEFT DIRECTION MEANS FALSE
            if(i<x){
                direction = true;                   //  MOVE IN RIGHT DIRECTION MEANS TRUE
            }
            while(true){
                try {
                    Thread.sleep(5);
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
                indicatorDate.setBounds(i,r.y,r.width,r.height);
                if(direction){
                    if(i>x){
                        break;
                    }
                    i+= i*velocity;
                }else{
                    if(i<x)
                        break;
                    i-= i*velocity;
                }
            }
        }).start();
    }


